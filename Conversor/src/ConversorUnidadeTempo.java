public class ConversorUnidadeTempo {
	
	public static int ConverteMinutosParaSegundos(int minutos){
		return minutos * 60;
	}
	
	public static int ConverteHorasParaMinutos(int horas){
		return horas * 60;
	}
	
	public static int ConverteDiasParaHoras(int dias){
		return dias * 24;
	}
	
	public static int ConverteSemanasParaDias(int semanas){
		return semanas * 7;
	}
	
	public static int ConverteMesesParaDias(int meses){
		return meses * 30;
	}
	
	public static int ConverteAnosParaDias(int anos){
		return anos * 365;
	}
	
}

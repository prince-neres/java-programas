public class Calculadora {
	
	public static void soma2(int n1, int n2) {
		System.out.println(n1 + " + " + n2 + " = " + (n1+n2));
	}
	
	public static void soma3(int n1, int n2, int n3) {
		System.out.println(n1 + " + " + n2 + " + " + n3 + " = " + (n1+n2+n3));
	}
	
	public static void subtrai2(int n1, int n2) {
		System.out.println(n1 + " - " + n2 + " = " + (n1-n2));
	}
	
	public static void subtrai3(int n1, int n2, int n3) {
		System.out.println(n1 + " - " + n2 + " - " + n3 + " = " + (n1-n2-n3));
	}
	
	public static void multiplica2(int n1, int n2) {
		System.out.println(n1 + " * " + n2 + " = " + (n1*n2));
	}
	
	public static void multiplica3(int n1, int n2, int n3) {
		System.out.println(n1 + " * " + n2 + " * " + n3 + " = " + (n1*n2*n3));
	}
	
	public static void divide2(int n1, int n2) {
		System.out.println(n1 + " / " + n2 + " = " + (n1/n2));
	}
	
	public static void divide3(int n1, int n2, int n3) {
		System.out.println(n1 + " / " + n2 + " / " + n3 + " = " + (n1/n2/n3));
	}
}
